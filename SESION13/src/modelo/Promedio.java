package modelo;

public class Promedio {
    public double promedio(double sueldo1,double sueldo2){
        return (sueldo1+sueldo2)/2;
    }
    
    public double promedio(double sueldo1,double sueldo2,double sueldo3){
        return (sueldo1+sueldo2+sueldo3)/3;
    }
    
    public double promedio(double sueldo1,double sueldo2,double sueldo3,double sueldo4){
        return (sueldo1+sueldo2+sueldo3+sueldo4)/4;
    }
}
